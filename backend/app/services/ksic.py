def ksic_name(code: str | None, *, short: bool = False) -> str | None:
    """코드 → 한글명(없으면 None). short=True 면 배지용 단축명."""
    if not code:
        return None
    table = _SHORT if short else _NAME
    return table.get(code, _NAME.get(ETC) if not short else _SHORT.get(ETC))


def is_valid_code(code: str | None) -> bool:
    return bool(code) and code in _NAME


