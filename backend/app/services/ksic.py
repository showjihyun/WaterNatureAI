# 키워드 동점 시 우선순위(도메인 특정 → 범용). 앞쪽이 우선.
_PRIORITY: list[str] = ["E", "Q", "F", "P", "A", "H", "D", "C", "J", "M", "R", "G", "N"]
_PRIORITY_RANK: dict[str, int] = {c: i for i, c in enumerate(_PRIORITY)}

# 소스 category(유형) prior — 유형 자체가 업종 신호를 줄 때만.
#   '공사'는 건설(F) 신뢰 신호. '물품/외자'는 모든 업종에 걸쳐(의료물품=Q, IT물품=J…)
#   업종 신호가 아니므로 prior 없음(키워드 없으면 ETC 가 정직).
_CATEGORY_PRIOR: dict[str, tuple[str, int]] = {
    "공사": ("F", 2),
}


def _hit_score(text_title: str, text_body: str, kws: Iterable[str]) -> int:
    """제목 등장=2점, 내용만 등장=1점. 키워드별 1회만(중복 가중 방지)."""
    score = 0
    for kw in kws:
        if kw in text_title:
            score += 2
        elif kw in text_body:
            score += 1
    return score


def classify_industry(
    title: str | None,
    body: str | None = None,
    source: str | None = None,
    category: str | None = None,
) -> str:
    """제목/내용을 KSIC 대분류로 분류해 코드 반환. 신호 없으면 ETC.

    절대 None 을 반환하지 않는다(필터/집계 버킷 일관성). 모르면 'ETC'.
    """
    t = (title or "").replace(" ", "")
    b = (body or "").replace(" ", "")
    if not t and not b:
        return ETC

    scores: dict[str, int] = {}
    for code, kws in KEYWORDS.items():
        s = _hit_score(t, b, kws)
        if s:
            scores[code] = s

    # 유형 prior 가산(키워드 신호를 보강하되 단독 결정은 약하게).
    if category:
        prior = _CATEGORY_PRIOR.get(category.strip())
        if prior:
            code, bonus = prior
            scores[code] = scores.get(code, 0) + bonus

    if not scores:
        return ETC

    # 최고점 → 동점은 우선순위로.
    best = max(
        scores.items(),
        key=lambda kv: (kv[1], -_PRIORITY_RANK.get(kv[0], 99)),
    )
    return best[0]
